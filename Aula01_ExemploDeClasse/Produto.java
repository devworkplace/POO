class Produto{
  
  int codigo;
  String nome;
  double preco;

  int getCodigo() {
    return codigo;
  }
  void setCodigo(int codigo) {
    this.codigo = codigo;
  }
  String getNome() {
    return nome;
  }
  void setNome(String nome) {
    this.nome = nome;
  }
  double getPreco() {
    return preco;
  }
  void setPreco(double preco) {
    this.preco = preco;
  } 

}